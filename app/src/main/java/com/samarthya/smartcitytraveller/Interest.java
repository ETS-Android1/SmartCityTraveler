package com.samarthya.smartcitytraveller;

public class Interest
{

	String name;
	String type;
	String distance;
	String lat;
	String lng;

	public Interest(String name, String type, String distance, String lat, String lng)
	{

		this.name = name;
		this.type = type;
		this.distance = distance;
		this.lat = lat;
		this.lng = lng;

	}

}
